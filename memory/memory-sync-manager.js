#!/usr/bin/env node

/**
 * Memory-CTB Sync Manager (Memory-P2 Part 2)
 *
 * Manages automatic synchronization between memory system and CTB (Current Task Board):
 * - Detects memory changes
 * - Updates CTB state atomically
 * - Maintains sync history
 * - Handles conflicts and retries
 */

const fs = require('fs');
const path = require('path');
const crypto = require('crypto');
const { execSync } = require('child_process');

const WORKSPACE_DIR = '/home/jeepney/.openclaw/workspace-dev';
const MEMORY_DIR = path.join(WORKSPACE_DIR, 'memory');
const COLLECTED_DIR = path.join(MEMORY_DIR, 'collected');
const SYNC_LOG = path.join(COLLECTED_DIR, 'sync_history.jsonl');
const SYNC_STATE = path.join(COLLECTED_DIR, 'sync_state.json');
const CTB_STATE = path.join(WORKSPACE_DIR, '.ctb-state.json');

const SYNC_INTERVAL_MS = 5 * 60 * 1000; // 5 minutes
const MAX_RETRIES = 3;

class MemorySyncManager {
  constructor() {
    this.syncState = {
      lastSync: null,
      lastMemoryHash: null,
      syncCount: 0,
      failureCount: 0,
      lastError: null,
      conflictResolutions: 0
    };
    this.loadSyncState();
  }

  /**
   * Load sync state from disk
   */
  loadSyncState() {
    if (fs.existsSync(SYNC_STATE)) {
      try {
        this.syncState = JSON.parse(fs.readFileSync(SYNC_STATE, 'utf-8'));
      } catch (err) {
        console.warn('[SYNC] Failed to load sync state:', err.message);
      }
    }
  }

  /**
   * Save sync state to disk
   */
  saveSyncState() {
    try {
      fs.mkdirSync(COLLECTED_DIR, { recursive: true });
      fs.writeFileSync(SYNC_STATE, JSON.stringify(this.syncState, null, 2));
    } catch (err) {
      console.error('[SYNC] Failed to save sync state:', err.message);
    }
  }

  /**
   * Get current memory hash
   */
  getMemoryHash() {
    const memoryFile = path.join(MEMORY_DIR, 'MEMORY.md');
    if (!fs.existsSync(memoryFile)) return null;

    const content = fs.readFileSync(memoryFile, 'utf-8');
    return crypto.createHash('md5').update(content).digest('hex');
  }

  /**
   * Check if memory has changed
   */
  hasMemoryChanged() {
    const currentHash = this.getMemoryHash();
    if (!currentHash) return false;

    if (!this.syncState.lastMemoryHash) {
      return true; // First sync
    }

    return currentHash !== this.syncState.lastMemoryHash;
  }

  /**
   * Load memory index (generated by memory-indexer.js)
   */
  loadMemoryIndex() {
    const indexFile = path.join(COLLECTED_DIR, 'memory_index.json');
    if (!fs.existsSync(indexFile)) return null;

    try {
      return JSON.parse(fs.readFileSync(indexFile, 'utf-8'));
    } catch (err) {
      console.error('[SYNC] Failed to load memory index:', err.message);
      return null;
    }
  }

  /**
   * Calculate memory system metrics
   */
  calculateMemoryMetrics() {
    const index = this.loadMemoryIndex();
    if (!index) return null;

    const metrics = {
      totalEntries: index.entries ? index.entries.length : 0,
      categories: index.categories ? Object.keys(index.categories).length : 0,
      lastIndexed: index.metadata ? index.metadata.lastIndexed : null
    };

    if (index.categories) {
      metrics.categoryBreakdown = {};
      for (const [cat, ids] of Object.entries(index.categories)) {
        metrics.categoryBreakdown[cat] = Array.isArray(ids) ? ids.length : 0;
      }
    }

    return metrics;
  }

  /**
   * Read current CTB state
   */
  readCTBState() {
    if (!fs.existsSync(CTB_STATE)) return null;

    try {
      return JSON.parse(fs.readFileSync(CTB_STATE, 'utf-8'));
    } catch (err) {
      console.error('[SYNC] Failed to read CTB state:', err.message);
      return null;
    }
  }

  /**
   * Update CTB state with memory metrics (atomic write)
   */
  updateCTBState(metrics) {
    const ctbState = this.readCTBState() || {};

    // Add memory system metrics to CTB
    ctbState.memory = {
      metrics,
      lastUpdated: new Date().toISOString(),
      syncCount: this.syncState.syncCount
    };

    ctbState.last_update = new Date().toLocaleString('ko-KR', {
      timeZone: 'Asia/Seoul'
    });

    try {
      const tmpFile = `${CTB_STATE}.tmp`;
      fs.writeFileSync(tmpFile, JSON.stringify(ctbState, null, 2));
      fs.renameSync(tmpFile, CTB_STATE);
      return true;
    } catch (err) {
      console.error('[SYNC] Failed to update CTB state:', err.message);
      return false;
    }
  }

  /**
   * Log sync event to JSONL history file
   */
  logSyncEvent(event) {
    try {
      fs.mkdirSync(COLLECTED_DIR, { recursive: true });
      const logEntry = {
        timestamp: new Date().toISOString(),
        ...event
      };
      fs.appendFileSync(SYNC_LOG, JSON.stringify(logEntry) + '\n');
    } catch (err) {
      console.error('[SYNC] Failed to log sync event:', err.message);
    }
  }

  /**
   * Execute sync with retry logic
   */
  executeSync(attempt = 1) {
    if (!this.hasMemoryChanged()) {
      console.log('[SYNC] Memory unchanged, skipping sync');
      this.logSyncEvent({
        status: 'skipped',
        reason: 'no_changes',
        attempt
      });
      return true;
    }

    try {
      console.log(`[SYNC] Running sync attempt ${attempt}/${MAX_RETRIES}...`);

      // Ensure indexer runs first
      this.runIndexer();

      // Calculate metrics
      const metrics = this.calculateMemoryMetrics();
      if (!metrics) {
        throw new Error('Failed to calculate metrics');
      }

      // Update CTB state atomically
      const updateSuccess = this.updateCTBState(metrics);
      if (!updateSuccess) {
        throw new Error('CTB update failed');
      }

      // Update sync state
      this.syncState.lastSync = new Date().toISOString();
      this.syncState.lastMemoryHash = this.getMemoryHash();
      this.syncState.syncCount++;
      this.syncState.failureCount = 0;
      this.syncState.lastError = null;

      this.saveSyncState();

      console.log('[SYNC] ✅ Sync completed successfully');
      this.logSyncEvent({
        status: 'success',
        syncCount: this.syncState.syncCount,
        metrics,
        attempt
      });

      return true;
    } catch (err) {
      console.error(`[SYNC] ❌ Sync failed (attempt ${attempt}):`, err.message);

      this.syncState.lastError = err.message;
      this.syncState.failureCount++;

      this.logSyncEvent({
        status: 'failed',
        error: err.message,
        attempt,
        failureCount: this.syncState.failureCount
      });

      // Retry if not exceeded max retries
      if (attempt < MAX_RETRIES) {
        const backoffMs = Math.pow(2, attempt - 1) * 1000;
        console.log(`[SYNC] Retrying in ${backoffMs}ms...`);
        setTimeout(() => this.executeSync(attempt + 1), backoffMs);
        return false;
      }

      this.saveSyncState();
      return false;
    }
  }

  /**
   * Run memory indexer before sync
   */
  runIndexer() {
    try {
      const indexerPath = path.join(MEMORY_DIR, 'memory-indexer.js');
      if (!fs.existsSync(indexerPath)) {
        throw new Error('Indexer not found');
      }

      const output = execSync(`node ${indexerPath}`, {
        encoding: 'utf-8',
        stdio: ['pipe', 'pipe', 'pipe']
      });

      console.log('[SYNC] Indexer output:', output.substring(0, 100));
    } catch (err) {
      console.error('[SYNC] Indexer failed:', err.message);
      throw err;
    }
  }

  /**
   * Get sync statistics
   */
  getStats() {
    return {
      lastSync: this.syncState.lastSync,
      syncCount: this.syncState.syncCount,
      failureCount: this.syncState.failureCount,
      successRate: this.syncState.syncCount > 0
        ? ((this.syncState.syncCount - this.syncState.failureCount) / this.syncState.syncCount * 100).toFixed(2) + '%'
        : 'N/A',
      lastError: this.syncState.lastError,
      conflictResolutions: this.syncState.conflictResolutions
    };
  }

  /**
   * Initialize continuous sync (for daemon mode)
   */
  initContinuousSync() {
    console.log('[SYNC] Starting continuous sync daemon');

    // Initial sync
    this.executeSync();

    // Recurring sync
    setInterval(() => {
      try {
        this.executeSync();
      } catch (err) {
        console.error('[SYNC] Daemon error:', err.message);
      }
    }, SYNC_INTERVAL_MS);

    // Keep process alive
    if (require.main === module) {
      console.log('[SYNC] Daemon running. Press Ctrl+C to stop.');
      process.on('SIGINT', () => {
        console.log('[SYNC] Shutting down gracefully...');
        this.saveSyncState();
        process.exit(0);
      });
    }
  }
}

// Main execution
if (require.main === module) {
  const manager = new MemorySyncManager();

  // Check command line arguments
  const cmd = process.argv[2];

  if (cmd === 'daemon') {
    manager.initContinuousSync();
  } else if (cmd === 'once') {
    manager.executeSync();
    setTimeout(() => process.exit(0), 1000);
  } else if (cmd === 'stats') {
    console.log('=== Sync Statistics ===');
    console.log(JSON.stringify(manager.getStats(), null, 2));
  } else {
    // Default: single sync attempt
    const success = manager.executeSync();
    process.exit(success ? 0 : 1);
  }
}

module.exports = MemorySyncManager;
